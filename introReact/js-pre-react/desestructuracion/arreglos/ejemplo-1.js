// La desestructuración de arreglos en JavaScript es una característica que te permite extraer valores de un arreglo y asignarlos a variables individuales de manera más conveniente. 

// Asignación de valores a variables individuales:

const arreglo = [1, 2, 3];

// Desestructuración
const [primerValor, segundoValor, tercerValor] = arreglo;

console.log(primerValor); // Resultado: 1
console.log(segundoValor); // Resultado: 2
console.log(tercerValor); // Resultado: 3
