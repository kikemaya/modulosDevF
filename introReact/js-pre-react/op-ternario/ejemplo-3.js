// Uso en una declaración return para devolver un valor en función de una condición:

function esPar(numero) {
  return numero % 2 === 0 ? 'Es par' : 'Es impar';
}

console.log(esPar(4)); // Resultado: 'Es par'
console.log(esPar(7)); // Resultado: 'Es impar'

// En esta función, el operador ternario se utiliza para determinar si un número es par o impar y devolver el resultado correspondiente.