// El operador ternario es una forma concisa de escribir una declaración condicional en JavaScript. Aquí tienes cinco ejemplos de cómo se utiliza el operador ternario:

// Asignación de un valor en función de una condición:

const edad = 18;
const mensaje = edad >= 18 ? 'Eres mayor de edad' : 'Eres menor de edad';
console.log(mensaje); // Resultado: 'Eres mayor de edad'

// En este ejemplo, el valor de mensaje se asigna en función de si la variable edad es mayor o igual a 18.