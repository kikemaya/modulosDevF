// El método `forEach()` en JavaScript se utiliza para iterar sobre los elementos de un array y ejecutar una función para cada elemento, pero a diferencia del método `map()`, no devuelve un nuevo array. Aquí tienes tres ejemplos de cómo se utiliza el método `forEach()`:

// 1. Iterar y mostrar cada elemento de un array:

javascript
const frutas = ['manzana', 'pera', 'plátano'];

frutas.forEach(fruta => {
  console.log(fruta);
});


// En este ejemplo, `forEach()` itera a través de cada elemento del array `frutas` y ejecuta una función que imprime cada elemento en la consola.