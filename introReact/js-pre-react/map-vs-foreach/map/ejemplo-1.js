// El método map() en JavaScript se utiliza para crear un nuevo array a partir de otro array, aplicando una función a cada elemento del array original. Aquí tienes tres ejemplos de cómo se utiliza el método map():

// Duplicar todos los elementos de un array:

const numeros = [1, 2, 3, 4, 5];

const duplicados = numeros.map(numero => numero * 2);

console.log(duplicados); // Resultado: [2, 4, 6, 8, 10]

// En este ejemplo, el método map() se utiliza para duplicar cada elemento del array numeros mediante la función que multiplica cada número por 2.