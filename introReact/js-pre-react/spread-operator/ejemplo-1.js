// El operador de propagación o "spread operator" (...) en JavaScript se utiliza para expandir elementos de un iterable (como un array) o las propiedades de un objeto en lugares donde se requieren múltiples elementos o propiedades.

// Clonar un array:

// Puedes utilizar el operador de propagación para crear una copia de un array existente.

const originalArray = [1, 2, 3];
const copiaArray = [...originalArray];

console.log(copiaArray); // Salida: [1, 2, 3]